def create_midi_art(text, filename="letters.mid", bpm=120):
    mid = MidiFile(ticks_per_beat=480)
    track = MidiTrack()
    mid.tracks.append(track)

    track.append(MetaMessage('set_tempo', tempo=int(60000000 / bpm)))

    base_note = 60  # C4
    pixel_duration = 120  # Длительность "пикселя"
    letter_spacing = 150  # Промежуток между буквами (1/4 ноты)

    # Флаг для первой буквы (не добавляем перед ней паузу)
    first_letter = True

    for letter in text.upper():
        pattern = LETTER_PATTERNS.get(letter, LETTER_PATTERNS[' '])

        # Добавляем промежуток перед буквой (кроме первой)
        if not first_letter:
            track.append(Message('note_off', note=0, velocity=0, time=letter_spacing))
        first_letter = False

        # Отрисовка буквы
        for col in range(5):
            # Собираем ноты текущего столбца
            notes_in_column = [base_note + (4 - row)
                               for row in range(5)
                               if pattern[row][col]]

            # Включаем ноты
            for note in notes_in_column:
                track.append(Message('note_on', note=note, velocity=64, time=0))

            # Выключаем ноты через заданное время
            if notes_in_column:
                track.append(Message('note_off', note=notes_in_column[0], velocity=0, time=pixel_duration))
                for note in notes_in_column[1:]:
                    track.append(Message('note_off', note=note, velocity=0, time=0))
            else:
                track.append(Message('note_off', note=0, velocity=0, time=pixel_duration))

    mid.save(filename)
    print(f"MIDI-арт сохранён в {filename}")


